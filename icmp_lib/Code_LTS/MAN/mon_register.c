UNS32 registers_index;
UNS32 TAB_registers_initialization[K_NUMBER_OF_REGISTERS];


/***************************************************************/
/* Global function : InitializeRegisterMonitoring              */
/***************************************************************/
void InitializeRegisterMonitoring(void)
{
    UNS32 i;

    /* Initialize local data*/
    registers_index = (UNS32)0;
    for (i = 0 ; i < K_NUMBER_OF_REGISTERS ; i ++)
    {
        TAB_registers_initialization[i] = (UNS32)0;
    }

    /* Initialize global data */
    /*! None */
}


/***************************************************************/
/* Global function : GetRegistersInformation                   */
/***************************************************************/
void GetRegistersInformation(void)
{
    UNS32 i;

    /* FOR all the registers to monitor */
    for (i = 0 ; i < K_NUMBER_OF_REGISTERS ; i ++)
    {
        /* Read the initial value of the register, filter the bits to monitor and save the result */
        switch (K_TAB_registers[i].size)
        {
            case 1:
                TAB_registers_initialization[i] = ((UNS32)(*((volatile UNS8  *)K_TAB_registers[i].address))) & K_TAB_registers[i].mask;
                break;

            case 2:
                TAB_registers_initialization[i] = ((UNS32)(*((volatile UNS16 *)K_TAB_registers[i].address))) & K_TAB_registers[i].mask;
                break;

            case 4:
                TAB_registers_initialization[i] = ((UNS32)(*((volatile UNS32 *)K_TAB_registers[i].address))) & K_TAB_registers[i].mask;
                break;

            default:
                /* Do nothing by default */
                break;
        }
    }
}


/***************************************************************/
/* Global function : MonitorRegisters                          */
/***************************************************************/
void MonitorRegisters(void)
{
    UNS32   i;
    UNS32   temp;
    volatile BOOLEAN F_error;

    /* FOR the next registers to monitor DO */
    i = 0;
    F_error = FALSE;
    while (i < K_NUMBER_OF_REGISTERS_TO_MONITOR_BY_PIT)
    {
        temp = 0;
        /* Read the current value of the register */
        switch (K_TAB_registers[registers_index].size)
        {
            case 1:
                temp = (UNS32)(*((volatile UNS8 *)K_TAB_registers[registers_index].address));
                break;

            case 2:
                temp = (UNS32)(*((volatile UNS16 *)K_TAB_registers[registers_index].address));
                break;

            case 4:
                temp = (UNS32)(*((volatile UNS32 *)K_TAB_registers[registers_index].address));
                break;

            default:
                /* Do nothing by default */
                break;
        }

        /* Filter the bits to monitor */
        temp = temp & K_TAB_registers[registers_index].mask;

        /* IF the current computed value is different from the value computed at initialization */
        if (temp != TAB_registers_initialization[registers_index])
        {
            /* Raise an error */
            F_error = TRUE;
        }
        else
        {
        }

        /* Get the next register to monitor */
        registers_index = (UNS32)((registers_index + 1u) % K_NUMBER_OF_REGISTERS);
        i ++;
    }

    /* eTPU internal memory monitoring : */
    /* Call MonitorARINCChannelsParameters */
    F_error |= MonitorARINCChannelsParameters();
    /* Call EtpuMonitorStepperMotorParameters */
    /*F_error |= EtpuMonitorStepperMotorParameters();*/
    /* Call EtpuMonitorQOMACMotorParameters */
    /*F_error |= EtpuMonitorQOMACMotorParameters();*/

    /* IF an error has been raised during the monitoring */
    if (F_error == TRUE)
    {
        /* Call DefaultHandlerUser function to reset the controller  */
        user_reset_source = SEU_REGISTERS;
        DefaultHandlerUser();
    }
    else
    {
    }
}